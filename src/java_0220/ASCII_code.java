package java_0220;

public class ASCII_code {
    public static void main(String[] args) {
        char a1 = 'A';
        char a2 = 65; // 指的是 ASCII 65 = A
        char at = 64; // 指的是 ASCII 64 = @
        System.out.printf("a1=%c\n", a1);
        System.out.printf("a1=%c\n", a2);
        System.out.printf("a1=%c\n", at);

        int a = '廖';
        int b = '卿';
        int c = '如';
        System.out.printf("a1=%d\n", a);
        System.out.printf("a1=%d\n", b);
        System.out.printf("a1=%d\n", c);
    }
}