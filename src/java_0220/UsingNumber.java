package java_0220;

public class UsingNumber {
    public static void main(String[] args) {
        short s = -13666;
        System.out.println("變數s = " +s);

        long l = 2_135_482_78910L; //記得尾巴要加上L
        System.out.println("變數l = " +l);

        int i = 0b1100_0110_0011_1010;
        System.out.println("變數i = " +i);
    }
}